function shuffle(a) { a = [...a]; for (let i = a.length - 1; i > 0; i--) { const j = Math.floor(Math.random() * (i + 1)); [a[i], a[j]] = [a[j], a[i]]; } return a; }

const existing = JSON.parse(fs.readFileSync(filePath, 'utf8'));
let idCounter = Math.max(...existing.map(q => Number(q.id.replace(/\D/g, '')))) + 1;
const prefix = existing[0].id.replace(/\d+$/, '');
const seenQ = new Set(existing.map(q => q.question));

const newItems = [];
Object.keys(CELLS).forEach(key => {
  const [gradeStr, diffStr] = key.split('-');
  const grade = Number(gradeStr), difficulty = Number(diffStr);
  CELLS[key].forEach(([question, answer, wrongs, meaning]) => {
    if (seenQ.has(question)) { console.log('SKIP dup:', question); return; }
    seenQ.add(question);
    const choices = shuffle([answer, ...wrongs]);
    newItems.push({
      id: `${prefix}${String(idCounter).padStart(3, '0')}`,
      question, answer, choices, meaning: meaning || answer, grade, difficulty,
    });
    idCounter++;
  });
});

const all = [...existing, ...newItems];
fs.writeFileSync(filePath, JSON.stringify(all, null, 2) + '\n', 'utf8');
console.log(`新規追加: ${newItems.length}問, 合計: ${all.length}問`);
