const fs = require('fs');
const path = require('path');

const FILE = path.join(__dirname, '..', 'data', 'sansu_bun.json');
const data = JSON.parse(fs.readFileSync(FILE, 'utf8'));

let nextIdNum = Math.max(...data.map(q => Number(q.id.slice(2)))) + 1;
function newId() { return 'sb' + String(nextIdNum++).padStart(3, '0'); }

function keepSubset(grade, diff, matchFn, count) {
  return data.filter(q => q.grade === grade && q.difficulty === diff && matchFn(q.question)).slice(0, count);
}

function gcd(a, b) { return b === 0 ? a : gcd(b, a % b); }

const newItems = [];
function add(grade, diff, question, answer, meaning) {
  newItems.push({ id: newId(), question, answer: String(answer), meaning, grade, difficulty: diff });
}

