// ============ ビルド ============
const data = JSON.parse(fs.readFileSync(FILE, 'utf8'));
let maxId = Math.max(...data.map(q => parseInt(q.id.replace('rp', ''), 10)));

const newItems = [];
for (let g = 1; g <= 6; g++) {
  for (let d = 1; d <= 4; d++) {
    const key = `${g}-${d}`;
    const arr = cells[key];
    if (!arr) throw new Error('missing cell ' + key);
    if (arr.length !== 20) throw new Error(`cell ${key} has ${arr.length} items, expected 20`);
    arr.forEach(([question, answer, w1, w2, w3, meaning]) => {
      maxId += 1;
      const id = 'rp' + String(maxId).padStart(3, '0');
      const choices = [answer, w1, w2, w3];
      // 選択肢の重複チェック
      const uniq = new Set(choices);
      if (uniq.size !== 4) throw new Error(`duplicate choices in ${id}: ${question}`);
      newItems.push({
        id,
        question,
        answer,
        choices,
        meaning,
        grade: g,
        difficulty: d
      });
    });
  }
}

const merged = data.concat(newItems);

// バリデーション
const seenQ = new Set();
for (const q of data) seenQ.add(q.question);
for (const q of newItems) {
  if (seenQ.has(q.question)) throw new Error('duplicate question text: ' + q.question);
  seenQ.add(q.question);
}

fs.writeFileSync(FILE, JSON.stringify(merged, null, 2) + '\n');
console.log('added', newItems.length, 'new items, total now', merged.length);
