// reason の末尾が「〜のは」「〜これを」「〜水に入れると」のような、
// 答えを無理やり文法的に接続させるための宙ぶらりんな語尾（助詞）になっている
// 場合、その「直前の。より後ろの断片」だけを対象に、宙ぶらりんな部分を
// 取りのぞく。（すでに完結している先行文には手を触れない）
const DANGLING_CHARS = /[はがをにへともでのならば]$/;
// 動詞・形容詞の連用形（て形の手前の形）で言い切れておらず、文が
// 中途半端に終わっている疑いのある語尾。こういう断片は使わず、
// 先行する完結文だけを採用する。
const RISKY_STEM = /[きぎしじちぢにひびぴみりえけげせぜてでねへべめれくず]$/;

function trimDanglingTail(tail) {
  let t = tail.trim();
  while (t && DANGLING_CHARS.test(t)) {
    const commaIdx = t.lastIndexOf('、');
    if (commaIdx >= 0) {
      t = t.slice(0, commaIdx).trim();
    } else {
      t = '';
      break;
    }
  }
  return t;
}

// 完結した先行文が1つもなく、断片も連用形で終わっている場合の最終手段。
// ごく一般的なパターンだけを丁寧な言い切りの形に変換する。
function completeConjugation(text) {
  let t = text.trim();
  if (!t) return 'このようなせいしつがあります';
  if (/(ため|から)$/.test(t)) return `${t}です`;
  if (/のは$/.test(t)) return `${t.slice(0, -2)}ことがあります`;
  if (/[はが]$/.test(t)) return `${t.slice(0, -1)}です`;
  if (/く$/.test(t)) return `${t.slice(0, -1)}いです`;
  if (/たり$/.test(t)) return `${t}します`;
  if (/[てで]$/.test(t)) return `${t}います`;
  if (/[をにへともでのならば]$/.test(t)) return `${t.slice(0, -1)}になります`;
  if (RISKY_STEM.test(t.slice(-1))) return `${t}ます`;
  return t;
}

function end(reason, answer) {
  let r = String(reason).trim().replace(/[。、]+$/, '');
  const lastMaru = r.lastIndexOf('。');
  const complete = lastMaru >= 0 ? r.slice(0, lastMaru + 1) : '';
  const rawTail = lastMaru >= 0 ? r.slice(lastMaru + 1) : r;
  let tail = trimDanglingTail(rawTail);
  const tailIsRisky = tail && RISKY_STEM.test(tail.slice(-1));

  if (tail && !tailIsRisky) {
    const body = `${complete}${tail}。`;
    return `${body}よって、答えは${answer}です。`;
  }
  if (complete) {
    // 断片が中途半端（連用形どまり等）でも、完結した先行文があるので
    // そちらだけを採用し、断片は切り捨てる。
    return `${complete}よって、答えは${answer}です。`;
  }
  // 完結文が1つもなく、断片自体も中途半端な場合の最終手段。
  const fb = completeConjugation(tail || rawTail);
  return `${fb}。よって、答えは${answer}です。`;
}

function q(item) { return item.question; }
function a(item) { return item.answer; }

