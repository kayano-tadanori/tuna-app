function g6_typeNakawaVol() {
  const mults = [];
  for (let v = 10; v <= 200; v += 5) mults.push(v);
  let a, b, x, guard = 0, ans;
  do {
    a = pick(mults.filter(v => v >= 20 && v <= 150));
    b = pick(mults.filter(v => v >= 10 && v <= 150));
    x = pick(mults.filter(v => v >= 10 && v <= 200));
    guard++;
  } while (((b * x) % a !== 0 || x === a || (b * x) / a <= 0 || (b * x) / a > 500) && guard < 3000);
  if ((b * x) % a !== 0) return null;
  ans = (b * x) / a;
  return {
    question: `うすい塩酸${a}cm³と水酸化ナトリウム水よう液${b}cm³がちょうど中和します。塩酸${x}cm³をちょうど中和させるには水酸化ナトリウム水よう液は何cm³必要？`,
    answer: String(ans),
    meaning: `${b}×${x}/${a}＝${ans}cm³`,
    choices: uniqShuffle4(ans, [a, b, x, ans + 10]),
  };
}
function g6_typeSaltMass() {
  const ratios = [[1, 5], [1, 4], [1, 8], [1, 10], [3, 10], [1, 6], [1, 2], [2, 5]];
  const [p, q] = pick(ratios);
  const k1 = randInt(2, 30), k2 = randInt(1, 40);
  if (k1 === k2) return null;
  const V = q * k1, M = p * k1;
  const v2 = q * k2, ans = p * k2;
  if (M <= 0 || ans <= 0) return null;
  return {
    question: `水酸化ナトリウム水よう液${V}cm³を塩酸でちょうど中和させると食塩が${M}gできます。同じ濃さの水酸化ナトリウム水よう液${v2}cm³をちょうど中和させると食塩は何gできる？`,
    answer: String(ans),
    meaning: `${M}×${v2}/${V}＝${ans}g`,
    choices: uniqShuffle4(ans, [M, V, v2, ans + 5]),
  };
}
function g6_typeExcessRemain() {
  const mults = [];
  for (let v = 10; v <= 150; v += 5) mults.push(v);
  let a, b, guard = 0;
  do { a = pick(mults); b = pick(mults); guard++; } while (guard < 500 && false);
  const extra = randInt(5, 12) * 5;
  const M = b + extra;
  return {
    question: `塩酸${a}cm³と水酸化ナトリウム水よう液${b}cm³がちょうど中和します。塩酸${a}cm³に水酸化ナトリウム水よう液${M}cm³を加えると、中和されずに残る水酸化ナトリウム水よう液は何cm³分？`,
    answer: String(extra),
    meaning: `${M}－${b}＝${extra}cm³`,
    choices: uniqShuffle4(extra, [M, b, a, extra + 10]),
  };
}

