function g4_solScale() {
  const vals = [8, 9, 12, 14, 16, 18, 20, 24, 27, 28, 32, 36, 40, 44, 48, 54, 56, 60, 64, 72];
  const waters = [25, 50, 75, 150, 200, 250, 300, 400, 500, 600];
  let val, w, ans;
  let guard = 0;
  do {
    val = pick(vals); w = pick(waters); guard++;
  } while ((val * w) % 100 !== 0 && guard < 500);
  if ((val * w) % 100 !== 0) return null;
  ans = (val * w) / 100;
  return {
    question: `20℃の水100gにあるものは${val}gまでとけます。同じ温度の水${w}gには何gまでとける？`,
    answer: String(ans),
    meaning: `${val}×${w}/100＝${ans}g`,
    choices: uniqShuffle4(ans, [val, w, ans + val, Math.round(ans / 2)]),
  };
}

