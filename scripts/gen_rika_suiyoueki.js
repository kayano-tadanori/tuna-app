function main() {
  const data = JSON.parse(fs.readFileSync(DATA_PATH, 'utf8'));
  const seen = new Set(data.map(q => q.question));
  let maxNum = 0;
  data.forEach(q => {
    const n = parseInt(q.id.replace('rs', ''), 10);
    if (n > maxNum) maxNum = n;
  });

  const newItems = [];
  let nextId = maxNum + 1;

  function pushKnowledge(pool, grade, difficulty) {
    pool.forEach(([q, a, c]) => {
      if (seen.has(q)) return; // 念のため重複防止
      seen.add(q);
      newItems.push({
        id: `rs${String(nextId).padStart(3, '0')}`,
        question: q,
        answer: a,
        choices: c,
        meaning: `（新規追加）`,
        grade, difficulty,
      });
      nextId++;
    });
  }
  function pushGen(genFn, n, grade, difficulty, args) {
    let arr;
    try {
      arr = genUnique(() => genFn(args), n, seen);
    } catch (e) {
      throw new Error(`${genFn.name} (grade${grade} diff${difficulty}, n=${n}): ${e.message}`);
    }
    arr.forEach(item => {
      newItems.push({
        id: `rs${String(nextId).padStart(3, '0')}`,
        question: item.question,
        answer: item.answer,
        choices: item.choices,
        meaning: item.meaning,
        grade, difficulty,
      });
      nextId++;
    });
  }

  // ---- 小4 ----
  pushKnowledge(g4KnowledgeD1, 4, 1);
  pushGen(g4_massAddTypeA, 6, 4, 1);

  pushKnowledge(g4KnowledgeD2, 4, 2);
  pushGen(g4_massAddTypeB, 3, 4, 2);
  pushGen(g4_massAddTypeC, 3, 4, 2);

  pushKnowledge(g4KnowledgeD3, 4, 3);
  pushGen(g4_solScale, 8, 4, 3);

  pushKnowledge(g4KnowledgeD4, 4, 4);
  pushGen(g4_solScale, 8, 4, 4);

  // ---- 小5 ----
  pushKnowledge(g5KnowledgeD1, 5, 1);
  pushGen(g5_typeNoudo, 8, 5, 1);
  pushGen(g5_typeSoluteFromPercent, 4, 5, 1);
  pushGen(g5_typeTotalFromPercentSolute, 3, 5, 1);

  pushKnowledge(g5KnowledgeD2, 5, 2);
  pushGen(g5_typeLeftover, 5, 5, 2);
  pushGen(() => g5_typeScale(false), 5, 5, 2);
  pushGen(() => g5_typeDilution(false), 5, 5, 2);

  pushKnowledge(g5KnowledgeD3, 5, 3);
  pushGen(g5_typeRecryst, 5, 5, 3);
  pushGen(g5_typeEvapPartial, 5, 5, 3);
  pushGen(g5_typeMix, 5, 5, 3);

  pushKnowledge(g5KnowledgeD4, 5, 4);
  pushGen(() => g5_typeDilution(true), 5, 5, 4);
  pushGen(g5_typeAddSolute, 5, 5, 4);
  pushGen(() => g5_typeScale(true), 5, 5, 4);

  // ---- 小6 ----
  pushKnowledge(g6KnowledgeD1, 6, 1);
  pushKnowledge(g6KnowledgeD2, 6, 2);

  pushKnowledge(g6KnowledgeD3, 6, 3);
  pushGen(g6_typeNakawaVol, 10, 6, 3);
  pushGen(g6_typeExcessRemain, 5, 6, 3);

  pushKnowledge(g6KnowledgeD4, 6, 4);
  pushGen(g6_typeSaltMass, 8, 6, 4);
  pushGen(g6_typeNakawaVol, 4, 6, 4);
  pushGen(g6_typeExcessRemain, 3, 6, 4);

  // ---- カウント確認 ----
  const cellCounts = {};
  newItems.forEach(it => {
    const k = `${it.grade}-${it.difficulty}`;
    cellCounts[k] = (cellCounts[k] || 0) + 1;
  });
  console.log('新規セルごとの件数:', cellCounts);
  console.log('新規合計:', newItems.length);

  const finalData = data.concat(newItems);
  fs.writeFileSync(DATA_PATH, JSON.stringify(finalData, null, 2) + '\n', 'utf8');
  console.log('書き込み完了。合計:', finalData.length);
}

main();
