// パターンC: P波・S波の速さときょり→初期び動継続時間
function typeContinuation() {
  const sv = pick([2, 3, 4]);
  let pv = pick([5, 6, 7, 8, 9, 10]);
  if (pv <= sv) pv = sv + 3;
  const k = randInt(2, 10);
  const d = pv * sv * k;
  const cont = (pv - sv) * k;
  const distractors = [cont + 5, cont - 3, Math.round(cont * 1.5)];
  return {
    question: `P波は秒速${pv}km、S波は秒速${sv}kmです。震源から${d}kmの地点での初期び動継続時間は何秒？`,
    answer: String(cont),
    meaning: `S波${d}÷${sv}＝${d / sv}秒－P波${d}÷${pv}＝${d / pv}秒＝${cont}秒`,
    choices: numChoices(cont, distractors),
  };
}

// パターンD: 初期び動継続時間→震源からのきょり
function typeDistanceFromContinuation() {
  const sv = pick([2, 3, 4]);
  let pv = pick([5, 6, 7, 8, 9, 10]);
  if (pv <= sv) pv = sv + 3;
  const m = randInt(2, 10);
  const t = (pv - sv) * m;
  const dist = m * pv * sv;
  const distractors = [dist + 20, dist - 15, Math.round(dist * 1.3)];
  return {
    question: `P波は秒速${pv}km、S波は秒速${sv}kmです。初期び動継続時間が${t}秒の地点は、震源から何km？`,
    answer: String(dist),
    meaning: `1kmあたりの継続時間差＝1/${sv}－1/${pv}秒。${t}÷それ＝${dist}km`,
    choices: numChoices(dist, distractors),
  };
}

// パターンI: 大森公式的な比例（1地点の実測値から別地点のきょりを逆算）
function typeOmoriProportion() {
  const k0 = pick([2, 3, 4, 5, 6]); // km/秒（この地震の大森定数）
  const t1 = randInt(5, 25);
  const d1 = k0 * t1;
  let t2 = randInt(5, 25);
  while (t2 === t1) t2 = randInt(5, 25);
  const d2 = k0 * t2;
  const distractors = [d2 + 10, d2 - 8, Math.round(d2 * 1.4)];
  return {
    question: `ある地震で、震源から${d1}kmの地点の初期び動継続時間は${t1}秒でした。同じ地震で、初期び動継続時間が${t2}秒だった地点は、震源から何kmですか？（初期び動継続時間と震源からのきょりは比例します）`,
    answer: String(d2),
    meaning: `${d1}÷${t1}＝${k0}（km/秒、大森定数）。${k0}×${t2}＝${d2}km`,
    choices: numChoices(d2, distractors),
  };
}

// パターンJ: 大森定数（きょり÷継続時間）を求める
function typeOmoriConstant() {
  const k = pick([2, 3, 4, 5, 6, 7, 8]);
  const t = randInt(4, 20);
  const d = k * t;
  const distractors = [k + 1, k - 1, k + 2];
  return {
    question: `ある地震で、震源から${d}kmの地点の初期び動継続時間は${t}秒でした。この地震の大森定数（きょり÷継続時間）はいくつですか？`,
    answer: String(k),
    meaning: `${d}÷${t}＝${k}`,
    choices: numChoices(k, distractors),
  };
}

// パターンE: P波到達時刻（時計の時こく）から地震発生時こくを逆算
function typeOriginFromP() {
  const v = pick([4, 5, 6, 7, 8, 9, 10]);
  const travel = randInt(5, 30);
  const d = v * travel;
  const origin = randInt(3, 25);
  const arrival = travel + origin;
  const distractors = [origin + 5, origin - 3, arrival];
  return {
    question: `P波は秒速${v}kmです。震源から${d}km地点にP波が届いたのは10時0分${arrival}秒でした。地震が発生したのは10時0分何秒？`,
    answer: String(origin),
    meaning: `${arrival}－${travel}＝${origin}秒`,
    choices: numChoices(origin, distractors),
  };
}

