function randInt(a, b) { return Math.floor(Math.random() * (b - a + 1)) + a; }
function pick(arr) { return arr[randInt(0, arr.length - 1)]; }

// 数値問題用の4択作成（正解＋近い数値のダミー3つ、重複なし）
function numChoices(correct, distractors) {
  const set = new Set([String(correct)]);
  const chosen = [];
  for (const d of distractors) {
    const s = String(d);
    if (!set.has(s) && Number(s) >= 0) { set.add(s); chosen.push(s); }
    if (chosen.length >= 3) break;
  }
  let guard = 0;
  while (chosen.length < 3 && guard < 300) {
    guard++;
    const noise = Number(correct) + randInt(-15, 15) * (guard % 5 + 1) - (guard % 2 === 0 ? 0 : 1);
    const s = String(noise);
    if (noise >= 0 && !set.has(s)) { set.add(s); chosen.push(s); }
  }
  const all = [String(correct), ...chosen];
  for (let i = all.length - 1; i > 0; i--) {
    const j = randInt(0, i);
    [all[i], all[j]] = [all[j], all[i]];
  }
  return all;
}

function genUnique(genFn, n, seenGlobal) {
  const out = [];
  let tries = 0;
  while (out.length < n && tries < 30000) {
    tries++;
    const r = genFn();
    if (!r) continue;
    if (seenGlobal.has(r.question)) continue;
    seenGlobal.add(r.question);
    out.push(r);
  }
  if (out.length < n) throw new Error(`生成不足: ${n}問中${out.length}問 (${genFn.name})`);
  return out;
}

