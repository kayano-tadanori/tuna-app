// ---------- Parametrized generators (for verification demonstration) ----------
// 縮尺（scale）: given a scale denominator (e.g. 25000) and either map-cm or real-m,
// compute the other value. real_cm = map_cm * denom; map_cm = real_cm / denom.
function scaleMapToReal(denom, mapCm) {
  const realCm = mapCm * denom;
  return realCm; // in cm
}
function scaleRealToMap(denom, realCm) {
  return realCm / denom; // in cm
}

// 時差（time difference）: East longitude = positive, West longitude = negative.
// UTC offset (hours) = longitude / 15 (East positive, West negative).
// Time difference between two points = |offset1 - offset2|.
function utcOffset(lonDeg, isEast) {
  return (isEast ? 1 : -1) * (lonDeg / 15);
}
function timeDiffHours(lon1, east1, lon2, east2) {
  return Math.abs(utcOffset(lon1, east1) - utcOffset(lon2, east2));
}

// ---------- Independent verification pass ----------
// Re-derive numeric answers from the question text via regex, cross-check against `answer`.
function verifyCalculations(items) {
  const problems = [];
  for (const it of items) {
    const q = it.question;
    const a = it.answer;

    // Pattern: 縮尺 X分の1 ... map_cmcm ... 実さい 何m/何km
    let m = q.match(/(\d+)分の1[^0-9]*?(\d+(?:\.\d+)?)\s*cm[^、]*?実さい\s*何(m|km)/);
    if (m) {
      const denom = Number(m[1]);
      const mapCm = Number(m[2]);
      const unit = m[3];
      const realCm = scaleMapToReal(denom, mapCm);
      const realM = realCm / 100;
      const expect = unit === 'km' ? realM / 1000 : realM;
      const gotNum = parseFloat(a);
      if (!Number.isFinite(gotNum) || Math.abs(gotNum - expect) > 1e-6) {
        problems.push({ id: it.id, q, a, expect, note: 'scale map->real mismatch' });
      }
      continue;
    }

    // Pattern: 縮尺 X分の1 ... 実さい Ykm/Ym ... 地図上 何cm
    m = q.match(/(\d+)分の1[^0-9]*?実さい\s*(\d+(?:\.\d+)?)\s*(m|km)[^、]*?(?:地図上|地図で)[^0-9]*?何cm/);
    if (m) {
      const denom = Number(m[1]);
      const realVal = Number(m[2]);
      const unit = m[3];
      const realCm = unit === 'km' ? realVal * 100000 : realVal * 100;
      const expect = scaleRealToMap(denom, realCm);
      const gotNum = parseFloat(a);
      if (!Number.isFinite(gotNum) || Math.abs(gotNum - expect) > 1e-6) {
        problems.push({ id: it.id, q, a, expect, note: 'scale real->map mismatch' });
      }
      continue;
    }
  }
  return problems;
}

