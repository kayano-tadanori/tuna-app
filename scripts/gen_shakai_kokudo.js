const combined = existing.concat(newItems);

// Validate: no duplicate question text. Pre-existing duplicates entirely among the
// original 320 items (id < sk321) are reported separately as a pre-existing data
// issue (out of scope: existing items must be left untouched), but ANY duplicate
// touching a newly-added item (id >= sk321) is a hard failure.
const isNew = (id) => Number(id.slice(2)) >= 321;
const qSet = new Map();
const newDupes = [];
const preExistingDupes = [];
for (const it of combined) {
  if (qSet.has(it.question)) {
    const otherId = qSet.get(it.question);
    const entry = { q: it.question, ids: [otherId, it.id] };
    if (isNew(it.id) || isNew(otherId)) newDupes.push(entry);
    else preExistingDupes.push(entry);
  } else {
    qSet.set(it.question, it.id);
  }
}
if (preExistingDupes.length) {
  console.warn('PRE-EXISTING DUPLICATES (in original 320, not modified per instructions):', JSON.stringify(preExistingDupes, null, 2));
}
if (newDupes.length) {
  console.error('DUPLICATE QUESTIONS INVOLVING NEW ITEMS:', JSON.stringify(newDupes, null, 2));
  process.exit(1);
} else {
  console.log('Duplicate question check (new items): PASS (0 duplicates involving new items among ' + combined.length + ' total items)');
}

// Validate: choices
let choiceProblems = [];
for (const it of combined) {
  if (!Array.isArray(it.choices) || it.choices.length !== 4) choiceProblems.push({ id: it.id, issue: 'not 4 choices' });
  else {
    const uniq = new Set(it.choices);
    if (uniq.size !== 4) choiceProblems.push({ id: it.id, issue: 'not unique' });
    if (!it.choices.includes(it.answer)) choiceProblems.push({ id: it.id, issue: 'answer missing from choices' });
  }
}
if (choiceProblems.length) {
  console.error('CHOICE PROBLEMS:', JSON.stringify(choiceProblems, null, 2));
  process.exit(1);
} else {
  console.log('Choices check: PASS');
}

