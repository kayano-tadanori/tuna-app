function randInt(a, b) { return Math.floor(Math.random() * (b - a + 1)) + a; }

function uniqShuffle4(correct, distractorPool) {
  const set = new Set([String(correct)]);
  const ds = distractorPool.map(String).filter(d => !set.has(d));
  const chosen = [];
  for (const d of ds) {
    if (chosen.length >= 3) break;
    if (!set.has(d)) { set.add(d); chosen.push(d); }
  }
  let guard = 0;
  const numCorrect = Number(correct);
  while (chosen.length < 3 && guard < 200) {
    guard++;
    let noise;
    if (!isNaN(numCorrect) && String(correct).trim() !== '') {
      noise = Math.round((numCorrect + randInt(-20, 20) * (guard % 5 + 1)) * 100) / 100;
      if (noise === numCorrect) noise = numCorrect + guard;
    } else {
      noise = String(correct) + guard;
    }
    if (!set.has(String(noise))) { set.add(String(noise)); chosen.push(String(noise)); }
  }
  const all = [String(correct), ...chosen];
  for (let i = all.length - 1; i > 0; i--) {
    const j = randInt(0, i);
    [all[i], all[j]] = [all[j], all[i]];
  }
  return all;
}

const data = JSON.parse(fs.readFileSync(FILE, 'utf8'));
const seen = new Set(data.map(q => q.question));
let maxId = 0;
data.forEach(q => { const n = parseInt(q.id.replace('rd', ''), 10); if (n > maxId) maxId = n; });

const newItems = [];

function pushHand(list, grade, difficulty) {
  list.forEach(([q, a, c]) => {
    if (seen.has(q)) throw new Error('DUPLICATE HAND QUESTION: ' + q);
    if (!c.includes(a)) throw new Error('answer not in choices: ' + q);
    if (new Set(c).size !== 4) throw new Error('choices not 4 unique: ' + q);
    seen.add(q);
    newItems.push({ question: q, answer: a, choices: c, meaning: '', grade, difficulty });
  });
}

function pushCalc(q, answer, meaning, distractors, grade, difficulty) {
  if (seen.has(q)) throw new Error('DUPLICATE CALC QUESTION: ' + q);
  seen.add(q);
  const choices = uniqShuffle4(answer, distractors);
  newItems.push({ question: q, answer: String(answer), choices, meaning, grade, difficulty });
}

function fmt(x) {
  const r = Math.round(x * 10000) / 10000;
  return String(r);
}

