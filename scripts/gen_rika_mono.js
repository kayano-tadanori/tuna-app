const REACTIONS = [
  { reactant: '炭素', other: '酸素', product: '二酸化炭素', ratio: [3, 8, 11] },
  { reactant: '水素', other: '酸素', product: '水', ratio: [1, 8, 9] },
  { reactant: 'マグネシウム', other: '酸素', product: '酸化マグネシウム', ratio: [3, 2, 5] },
  { reactant: '銅', other: '酸素', product: '酸化銅', ratio: [4, 1, 5] },
  { reactant: '鉄', other: '酸素', product: '酸化鉄', ratio: [7, 3, 10] },
];

// テンプレートA：基準の1例を示し、別の量に換算
function typeCombustA() {
  const r = pick(REACTIONS);
  const [a, b, c] = r.ratio;
  const m2 = pick([2, 3, 4, 5, 6]);
  const baseReact = a, baseProd = c;
  const askReact = a * m2, askProd = c * m2;
  return {
    question: `${r.reactant}${baseReact}gが 完全に 燃えると ${r.product}${baseProd}gが できます。${r.reactant}${askReact}gでは ${r.product}は 何g できますか？`,
    answer: String(askProd),
    meaning: `${baseProd}g×(${askReact}÷${baseReact})＝${askProd}g`,
    choices: uniqShuffle4(askProd, [baseProd, askReact, askProd + b * m2, Math.round(askProd / 2)]),
  };
}

// テンプレートB：比を示し、結びつく酸素の量を問う
function typeCombustB() {
  const r = pick(REACTIONS);
  const [a, b, c] = r.ratio;
  const m2 = pick([2, 3, 4, 5, 6, 7]);
  const reactMass = a * m2;
  const ans = b * m2;
  return {
    question: `${r.reactant}と ${r.other}は ${a}：${b}の 重さの わりあいで むすびつきます。${r.reactant}${reactMass}gと ちょうど むすびつく ${r.other}は 何gですか？`,
    answer: String(ans),
    meaning: `${reactMass}g×(${b}÷${a})＝${ans}g`,
    choices: uniqShuffle4(ans, [reactMass, c * m2, Math.round(ans * 1.5), Math.max(1, ans - b)]),
  };
}

// テンプレートC：反応前後の質量から結びついた酸素量を問う
function typeCombustC() {
  const r = pick(REACTIONS);
  const [a, b, c] = r.ratio;
  const m2 = pick([2, 3, 4, 5, 6]);
  const reactMass = a * m2;
  const prodMass = c * m2;
  const ans = b * m2;
  return {
    question: `${r.reactant}${reactMass}gが 燃えて ${r.product}${prodMass}gに なりました。むすびついた ${r.other}は 何gですか？`,
    answer: String(ans),
    meaning: `${prodMass}g－${reactMass}g＝${ans}g`,
    choices: uniqShuffle4(ans, [reactMass, prodMass, Math.round(ans * 2), Math.max(1, Math.round(ans / 2))]),
  };
}

// d3: 酸素の割合の増減（引き算）
function typeOxygenPercentDiff() {
  const initP = randInt(19, 21);
  const dec = randInt(2, 8);
  const finalP = roundTo(initP - dec * 0.5, 1);
  const decActual = roundTo(initP - finalP, 1);
  return {
    question: `ある 集気びんの 空気は 酸素が ${initP}%でした。ろうそくを 燃やして 火が 消えた とき、酸素は ${finalP}%に なりました。減った 酸素の 割合は 何%ですか？`,
    answer: String(decActual),
    meaning: `${initP}－${finalP}＝${decActual}%`,
    choices: uniqShuffle4(decActual, [initP, finalP, roundTo(decActual * 2, 1)]),
  };
}

// d4: 質量保存（密閉容器内の燃焼前後の全体の重さ）
function typeMassConservation() {
  const reactMass = randInt(5, 40);
  const oMass = randInt(3, 30);
  const ans = reactMass + oMass;
  return {
    question: `密閉した 容器の 中で、ものX ${reactMass}gと 酸素${oMass}gが すべて むすびついて 反応しました。反応後に できた ものの 重さは 何gですか？（容器の 中の 全体の 重さは 変わりません）`,
    answer: String(ans),
    meaning: `${reactMass}g＋${oMass}g＝${ans}g（質量保存）`,
    choices: uniqShuffle4(ans, [reactMass, oMass, Math.abs(reactMass - oMass), ans + 5]),
  };
}

