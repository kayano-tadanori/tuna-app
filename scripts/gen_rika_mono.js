// d2/d3: 密度・体積・質量
function typeDensityMassVolume() {
  const kind = pick(['findMass', 'findVolume', 'findDensity']);
  const density = pick([0.8, 1, 1.2, 2, 2.7, 3, 4, 5, 7.9, 8, 9]);
  const volume = pick([5, 10, 15, 20, 25, 50, 100]);
  const massRaw = density * volume;
  const mass = roundTo(massRaw, 1);
  if (kind === 'findMass') {
    return {
      question: `1cm³あたり ${density}gの 液体（または 固体）が あります。この 物質 ${volume}cm³の 重さは 何gですか？`,
      answer: String(mass),
      meaning: `${density}g×${volume}cm³＝${mass}g`,
      choices: uniqShuffle4(mass, [density, volume, roundTo(mass + density, 1), roundTo(mass - volume, 1)]),
    };
  } else if (kind === 'findVolume') {
    return {
      question: `1cm³あたり ${density}gの 物質が あります。この 物質 ${mass}gの 体積は 何cm³ですか？`,
      answer: String(volume),
      meaning: `${mass}g÷${density}g＝${volume}cm³`,
      choices: uniqShuffle4(volume, [density, mass, volume + 5, Math.max(1, volume - 5)]),
    };
  } else {
    return {
      question: `体積${volume}cm³、重さ${mass}gの 物質が あります。1cm³あたりの 重さ（密度）は 何gですか？`,
      answer: String(density),
      meaning: `${mass}g÷${volume}cm³＝${density}g/cm³`,
      choices: uniqShuffle4(density, [roundTo(density + 1, 1), roundTo(density - 0.5, 1), roundTo(density * 2, 1)]),
    };
  }
}

