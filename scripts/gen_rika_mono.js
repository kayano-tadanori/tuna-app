// ---- 小3 密度比例・てんびん・重さの保存（20問中10問を生成） ----
function typeG3DensityScale() {
  const dens = randInt(2, 9);
  const baseVol = pick([5, 10, 15, 20]);
  const baseWeight = dens * baseVol;
  const mult = pick([2, 3, 4, 5]);
  const targetVol = baseVol * mult;
  const ans = baseWeight * mult;
  return {
    question: `同じ 材料で できた ものが あります。体積が ${baseVol}cm³で ${baseWeight}gの とき、体積が ${targetVol}cm³では 何gに なりますか？`,
    answer: String(ans),
    meaning: `${baseWeight}g×(${targetVol}÷${baseVol})＝${ans}g`,
    choices: uniqShuffle4(ans, [baseWeight, ans + mult, Math.round(ans / mult), ans - dens]),
  };
}

