function randInt(a, b) { return Math.floor(Math.random() * (b - a + 1)) + a; }
function pick(arr) { return arr[randInt(0, arr.length - 1)]; }
function roundTo(v, decimals) {
  const p = Math.pow(10, decimals);
  return Math.round(v * p) / p;
}

// 数値/文字列の答えから、重複しない3つのダミー選択肢を作る（rika_sora.jsのuniqShuffle4を踏襲）
function uniqShuffle4(correct, distractorPool) {
  const set = new Set([String(correct)]);
  const ds = distractorPool.map(String).filter(d => !set.has(d));
  const chosen = [];
  for (const d of ds) {
    if (chosen.length >= 3) break;
    if (!set.has(d)) { set.add(d); chosen.push(d); }
  }
  let guard = 0;
  const numCorrect = Number(correct);
  while (chosen.length < 3 && guard < 200) {
    guard++;
    let noise;
    if (!isNaN(numCorrect) && String(correct).trim() !== '') {
      noise = roundTo(numCorrect + randInt(-20, 20) * (guard % 5 + 1) * (Math.abs(numCorrect) < 5 ? 0.1 : 1), 2);
      if (noise === numCorrect) noise = numCorrect + guard;
    } else {
      noise = String(correct).replace(/\d+(\.\d+)?/, m => {
        const isDec = m.includes('.');
        let v = parseFloat(m) + randInt(-20, 20) * (guard % 5 + 1);
        if (isDec) v = Math.round(v * 10) / 10;
        return String(v);
      });
    }
    if (!set.has(String(noise))) { set.add(String(noise)); chosen.push(String(noise)); }
  }
  const all = [String(correct), ...chosen];
  for (let i = all.length - 1; i > 0; i--) {
    const j = randInt(0, i);
    [all[i], all[j]] = [all[j], all[i]];
  }
  return all;
}

function genUniqueCalc(genFn, n, seenGlobal) {
  const out = [];
  let tries = 0;
  while (out.length < n && tries < 40000) {
    tries++;
    const r = genFn();
    if (!r) continue;
    if (seenGlobal.has(r.question)) continue;
    seenGlobal.add(r.question);
    out.push(r);
  }
  if (out.length < n) throw new Error(`生成不足: ${n}問中${out.length}問 (${genFn.name})`);
  return out;
}

