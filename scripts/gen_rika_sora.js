// ===== 小6：日周運動＋年周運動の複合 =====
function typeFukugou() {
  const hDiff = randInt(1, 8);
  const mDiff = randInt(1, 6);
  const ans = hDiff * 15 + mDiff * 30;
  const distractors = [hDiff * 15, mDiff * 30, hDiff * 15 + mDiff * 15, hDiff * 30 + mDiff * 30];
  return {
    question: `ある年の7月1日20時に、北極星を中心に0°の位置に見えた星座がありました。この星座を${mDiff}か月後の8月1日から数えて、同じ日の20時から${hDiff}時間後の時こくに観察すると、0°の位置から何度動いた位置に見えますか？（日周運動：1時間に15度、年周運動：1か月に30度、どちらも反時計回りで角度を足し合わせて考えます）`,
    answer: String(ans),
    meaning: `日周運動15度×${hDiff}時間＋年周運動30度×${mDiff}か月＝${ans}度`,
    choices: uniqShuffle4(ans, distractors),
  };
}

// ===== 小5・6：太陽の南中高度 =====
function typeNanchuu(grade) {
  const lat = grade === 5 ? randInt(30, 40) : randInt(24, 45);
  const season = pick(['夏至', '冬至', '春分・秋分']);
  let ans;
  let formula;
  if (season === '夏至') { ans = 90 - lat + 23.4; formula = `90－${lat}＋23.4＝${ans}`; }
  else if (season === '冬至') { ans = 90 - lat - 23.4; formula = `90－${lat}－23.4＝${ans}`; }
  else { ans = 90 - lat; formula = `90－${lat}＝${ans}`; }
  ans = Math.round(ans * 10) / 10;
  const wrongSeason = season === '夏至' ? 90 - lat - 23.4 : (season === '冬至' ? 90 - lat + 23.4 : 90 - lat + 23.4);
  const distractors = [Math.round(wrongSeason * 10) / 10, 90 - lat, lat + 23.4, 90 - lat + (season === '夏至' ? -23.4 : 23.4)];
  return {
    question: `緯度${lat}度の地点における${season}の太陽の南中高度は何度ですか？（夏至は「90－緯度＋23.4」、冬至は「90－緯度－23.4」、春分・秋分は「90－緯度」で求められます）`,
    answer: String(ans),
    meaning: `${formula}度`,
    choices: uniqShuffle4(ans, distractors),
  };
}

// ===== 小5・6：経度と時差 =====
function typeKeido() {
  const base = 135;
  let lon = randInt(120, 150);
  while (lon === base) lon = randInt(120, 150);
  const diffMin = (lon - base) * 4; // 東経が大きい(東)ほど早い
  const ansMin = 12 * 60 - diffMin;
  const ans = fmtTime(ansMin);
  const distractors = [
    fmtTime(12 * 60 + diffMin),
    fmtTime(12 * 60 - (lon - base)),
    fmtTime(12 * 60 - diffMin * 2),
  ];
  return {
    question: `東経135度の地点で太陽が南中する時こくは12時00分です。東経${lon}度の地点で太陽が南中する時こくは何時何分ですか？（経度1度につき4分の差があり、東にあるほど南中時こくは早くなります）`,
    answer: ans,
    meaning: `経度差${Math.abs(lon - base)}度×4分＝${Math.abs(diffMin)}分${lon > base ? '早い' : 'おそい'}ので${ans}`,
    choices: uniqShuffle4(ans, distractors),
  };
}

// ===== 小5・6：昼の長さ =====
function typeHiruNagasa() {
  const sunriseH = randInt(4, 6), sunriseM = pick([0, 10, 20, 30, 40, 50]);
  const sunsetH = randInt(17, 19), sunsetM = pick([0, 10, 20, 30, 40, 50]);
  const sr = sunriseH * 60 + sunriseM;
  const ss = sunsetH * 60 + sunsetM;
  const durMin = ss - sr;
  const h = Math.floor(durMin / 60), m = durMin % 60;
  const ans = `${h}時間${m === 0 ? '0' : m}分`;
  const distractors = [
    `${h}時間${m === 0 ? '0' : (60 - m) % 60}分`,
    `${h - 1 >= 0 ? h - 1 : h}時間${m}分`,
    `${h + 1}時間${m}分`,
  ];
  return {
    question: `ある日の日の出は${sunriseH}時${String(sunriseM).padStart(2, '0')}分、日の入りは${sunsetH}時${String(sunsetM).padStart(2, '0')}分でした。この日の昼の長さは何時間何分ですか？`,
    answer: ans,
    meaning: `日の入り－日の出＝${ans}`,
    choices: uniqShuffle4(ans, distractors),
  };
}

// ===== 小5・6：透明半球の記録 =====
function typeToumeihankyuu() {
  const rate = pick([1, 1.5, 2, 3]); // cm/時間
  const hoursToNoon = randInt(2, 6);
  const arc = rate * hoursToNoon;
  const sunriseH = 12 - hoursToNoon;
  const ans = `${sunriseH}時`;
  const distractors = [`${12 + hoursToNoon}時`, `${hoursToNoon}時`, `${sunriseH - 1}時`];
  return {
    question: `透明半球に太陽の1日の動きを記録すると、1時間ごとの点の間かくは${rate}cmでした。日の出から南中（12時）までの弧の長さが${arc}cmのとき、日の出の時こくは何時ですか？`,
    answer: ans,
    meaning: `${arc}cm÷${rate}cm＝${hoursToNoon}時間前なので12時－${hoursToNoon}時間＝${ans}`,
    choices: uniqShuffle4(ans, distractors),
  };
}

function genUnique(genFn, n, seenGlobal) {
  const out = [];
  let tries = 0;
  while (out.length < n && tries < 20000) {
    tries++;
    const r = genFn();
    if (!r) continue;
    if (seenGlobal.has(r.question)) continue;
    seenGlobal.add(r.question);
    out.push(r);
  }
  if (out.length < n) throw new Error(`生成不足: ${n}問中${out.length}問 (${genFn.name})`);
  return out;
}

