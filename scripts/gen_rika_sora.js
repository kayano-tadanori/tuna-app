function knowFromList(list) {
  return list.map(([q, a, c]) => ({ question: q, answer: a, choices: c, meaning: a }));
}

// 生成
const filePath = path.join(__dirname, '..', 'data', 'rika_sora.json');
const existing = JSON.parse(fs.readFileSync(filePath, 'utf8'));
let idCounter = Math.max(...existing.map(q => Number(q.id.replace(/\D/g, '')))) + 1;
const prefix = existing[0].id.replace(/\d+$/, '');
const seenGlobal = new Set(existing.map(q => q.question));

function pushItems(items, grade, difficulty, out) {
  items.forEach(it => {
    out.push({
      id: `${prefix}${String(idCounter).padStart(3, '0')}`,
      question: it.question,
      answer: it.answer,
      choices: it.choices,
      meaning: it.meaning,
      grade, difficulty,
    });
    idCounter++;
  });
}

function fromKnowledge(list) {
  return list.map(k => ({ question: k.q, answer: k.a, choices: k.c, meaning: `${k.a}` }));
}

