function randInt(a, b) { return Math.floor(Math.random() * (b - a + 1)) + a; }
function pick(arr) { return arr[randInt(0, arr.length - 1)]; }

function uniqShuffle4(correct, distractorPool) {
  const set = new Set([String(correct)]);
  const ds = distractorPool.map(String).filter(d => !set.has(d));
  const chosen = [];
  for (const d of ds) {
    if (chosen.length >= 3) break;
    if (!set.has(d)) { set.add(d); chosen.push(d); }
  }
  // 足りなければランダムに埋める（correctの近い値。数値でなければ文字列中の数字をずらす）
  let guard = 0;
  const numCorrect = Number(correct);
  while (chosen.length < 3 && guard < 100) {
    guard++;
    let noise;
    if (!isNaN(numCorrect) && String(correct).trim() !== '') {
      noise = numCorrect + randInt(-20, 20) * (guard % 5 + 1);
    } else {
      noise = String(correct).replace(/\d+(\.\d+)?/, m => {
        const isDec = m.includes('.');
        let v = parseFloat(m) + randInt(-20, 20) * (guard % 5 + 1);
        if (isDec) v = Math.round(v * 10) / 10;
        return String(v);
      });
    }
    if (!set.has(String(noise))) { set.add(String(noise)); chosen.push(String(noise)); }
  }
  const all = [String(correct), ...chosen];
  // shuffle
  for (let i = all.length - 1; i > 0; i--) {
    const j = randInt(0, i);
    [all[i], all[j]] = [all[j], all[i]];
  }
  return all;
}

function fmtTime(totalMin) {
  let h = Math.floor(totalMin / 60);
  let m = totalMin % 60;
  h = ((h % 24) + 24) % 24;
  return `${h}時${m === 0 ? '00' : String(m).padStart(2, '0')}分`;
}

// ===== 小5：星の日周運動（時刻→位置） =====
function typeNichiJi() {
  const startH = randInt(19, 22);
  const dur = randInt(2, 8);
  const endH = startH + dur;
  const ans = dur * 15;
  const distractors = [dur * 10, dur * 30, 360 - dur * 15, dur * 15 + 15];
  return {
    question: `${startH}時に北極星を中心にして0°の位置に見えた星があります。この星は、北極星を中心に1時間に15度、反時計回りに動いて見えます。${endH}時には、0°の位置から何度回転した位置に見えますか？`,
    answer: String(ans),
    meaning: `1時間に15度×${dur}時間＝${ans}度`,
    choices: uniqShuffle4(ans, distractors),
  };
}

// ===== 小5：星の日周運動（角度→経過時間） =====
function typeNichiJiGyaku() {
  const dur = randInt(2, 10);
  const ang = dur * 15;
  const distractors = [ang / 10, ang * 15, Math.round(ang / 30 * 10) / 10, dur + 5];
  return {
    question: `北極星を中心に星を観察すると、1時間に15度、反時計回りに回転して見えます。ある星が90度から数えて${ang}度分だけ回転して見えました。何時間観察していたことになりますか？`,
    answer: String(dur),
    meaning: `${ang}度÷15度＝${dur}時間`,
    choices: uniqShuffle4(dur, distractors),
  };
}

