// ---- 生成・マージ処理 ----
const raw = fs.readFileSync(FILE, 'utf8');
const data = JSON.parse(raw);

// 現在の最大ID番号を取得
let maxNum = 0;
for (const item of data) {
  const m = /^sm(\d+)$/.exec(item.id);
  if (m) maxNum = Math.max(maxNum, parseInt(m[1], 10));
}

let nextNum = maxNum + 1;
const newItems = [];
let totalNew = 0;

for (const key of Object.keys(CELLS)) {
  const m = /^g(\d)d(\d)$/.exec(key);
  const grade = parseInt(m[1], 10);
  const difficulty = parseInt(m[2], 10);
  const arr = CELLS[key];
  for (const [question, answer, choices, meaning] of arr) {
    const id = 'sm' + String(nextNum).padStart(3, '0');
    nextNum++;
    if (new Set(choices).size !== 4 || !choices.includes(answer)) {
      throw new Error('Invalid choices for question: ' + question);
    }
    newItems.push({ id, question, answer, choices, meaning, grade, difficulty });
    totalNew++;
  }
}

const merged = data.concat(newItems);
fs.writeFileSync(FILE, JSON.stringify(merged, null, 2) + '\n', 'utf8');

console.log('Added', totalNew, 'new items. Total now', merged.length);
